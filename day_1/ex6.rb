# Declares variable with integer value.
types_of_people = 10

# Declares variable with interpolated string value.
x = "There are #{types_of_people} types of people."

# Declares variables with string values.
binary = "binary"
do_not = "don't"

# Declares variable with interpolated string values.
y = "Those who know #{binary} and those who #{do_not}."

# Prints string values of two variables with line breaks.
puts x
puts y

# Prints interpolated strings with line breaks.
puts "I said: #{x}."
puts "I also said: '#{y}'."

# Declare variable with boolean value.
hilarious = false

# Declare variable with interpolated string value.
joke_evaluation = "Isn't that joke so funny?! #{hilarious}"

# Print string value of variable.
puts joke_evaluation

# Declare two variables with string values.
w = "This is the left side of..."
e = "a string with a right side."

# Print concatenated string values of two variables.
puts w + e
